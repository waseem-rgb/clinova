def strip_citations_from_text(text: str) -> str:
    """Strip any citation references from text."""
    patterns = [
        r"\bSources?\s*:\s*.*?$",
        r"\[\d+\]",
        r"\(p\.?\s*\d+\)",
        r"\bpage\s+\d+\b",
        r"\bChapter\s+\d+",
        r"\bSee\s+also\b.*$",
        r"\bFurther\s+reading\b.*$",
    ]
    
    result = text
    for pattern in patterns:
        result = re.sub(pattern, "", result, flags=re.IGNORECASE | re.MULTILINE)
    
    result = re.sub(r"\n{3,}", "\n\n", result)
    return result.strip()


def ensure_section_complete(
    section_content: Any,
    section_type: str,
) -> Any:
    """
    Ensure a section has complete, non-empty content.
    If empty, return placeholder with instruction.
    """
    if _has_content(section_content):
        return section_content
    
    # Return appropriate placeholder based on section type
    placeholders = {
        "dose": [{"generic": "Verify dose", "dose": "VERIFY LOCALLY", "verify_dose_warning": True}],
        "regimen": [{"label": "Treatment regimen", "drugs": [], "note": "Insufficient evidence - consult local guidelines"}],
        "list": ["Insufficient evidence in sources - verify locally"],
        "text": "Insufficient evidence in sources - verify locally",
    }
    
    return placeholders.get(section_type, placeholders["list"])
