def _label_fallback_content(content: Dict[str, Any]) -> Dict[str, Any]:
    """Add LLM-guided labels to all fallback content."""
    content["_source_label"] = SourceLabel.LLM_GUIDED.value
    content["_is_fallback"] = True
    
    # Add warnings to dose fields
    def add_dose_warning(obj):
        if isinstance(obj, dict):
            if "dose" in obj and obj.get("dose"):
                obj["verify_dose_warning"] = True
                if not obj["dose"].endswith("- VERIFY LOCALLY"):
                    obj["dose"] = obj["dose"] + " - VERIFY LOCALLY"
            for v in obj.values():
                add_dose_warning(v)
        elif isinstance(obj, list):
            for item in obj:
                add_dose_warning(item)
    
    add_dose_warning(content)
    return content


def _empty_fallback_content(sections_needed: List[str]) -> Dict[str, Any]:
    """Return empty fallback content with structure."""
    content = {
        "_source_label": SourceLabel.LLM_GUIDED.value,
        "_is_fallback": True,
        "_fallback_failed": True,
        "llm_generated_warning": "Unable to generate fallback content. Please consult local guidelines.",
    }
    for section in sections_needed:
        content[section] = []
    return content


