# Regex patterns for extracting doses
DOSE_PATTERNS = [
    # Standard dose patterns
    r"(\d+(?:\.\d+)?)\s*(mg|mcg|g|ml|units?|iu)\s*(?:per\s+)?(?:dose|day)?",
    # Weight-based
    r"(\d+(?:\.\d+)?)\s*(mg|mcg|g|ml|units?)/kg",
    # Frequency patterns
    r"(\d+(?:\.\d+)?)\s*(mg|mcg|g|ml|units?)\s*(bd|tds|qds|qid|tid|bid|od|once\s+daily|twice\s+daily)",
    # Range patterns
    r"(\d+(?:\.\d+)?)\s*(?:to|-)\s*(\d+(?:\.\d+)?)\s*(mg|mcg|g|ml|units?)",
]


def extract_doses_from_text(text: str) -> List[Dict[str, Any]]:
    """
    Extract structured dose information from text.
    
    Returns list of dose objects with:
    - value: numeric dose value
    - unit: mg, mcg, g, etc.
    - frequency: if found
    - weight_based: True if per kg dose
    - raw_text: original matched text
    """
    doses = []
    text_lower = text.lower()
    
    for pattern in DOSE_PATTERNS:
        for match in re.finditer(pattern, text_lower, re.IGNORECASE):
            groups = match.groups()
            dose_obj = {
                "raw_text": match.group(0),
                "value": float(groups[0]) if groups[0] else None,
                "unit": groups[1] if len(groups) > 1 else None,
                "frequency": groups[2] if len(groups) > 2 else None,
                "weight_based": "/kg" in match.group(0),
            }
            doses.append(dose_obj)
    
    return doses


def validate_dose_reasonableness(
    drug_name: str,
    dose_value: float,
    unit: str,
) -> bool:
    """
    Basic validation that a dose is within reasonable range.
    Returns True if dose seems reasonable.
    """
    # Common drug max daily doses (simplified)
    MAX_DOSES = {
        "paracetamol": 4000,  # mg
        "acetaminophen": 4000,
        "ibuprofen": 2400,
        "amoxicillin": 6000,
        "metformin": 2550,
        "lisinopril": 80,
        "amlodipine": 10,
        "atorvastatin": 80,
        "omeprazole": 40,
        "aspirin": 4000,
    }
    
    drug_lower = drug_name.lower()
    for known_drug, max_dose in MAX_DOSES.items():
        if known_drug in drug_lower:
            if unit.lower() in ["mg", "milligram"]:
                return dose_value <= max_dose
    
    # Default: if dose seems very high, flag it
    if unit.lower() in ["mg", "milligram"] and dose_value > 10000:
        return False
    
    return True


