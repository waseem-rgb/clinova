# These prompts are used when RAG evidence is insufficient.
# They are designed to generate CONSERVATIVE, CLEARLY LABELED content.

TREATMENT_FALLBACK_SYSTEM = """You are a clinical decision support assistant generating CONSERVATIVE treatment guidance.

CRITICAL RULES:
1. This is FALLBACK content when textbook evidence is insufficient
2. Every response MUST include warnings to verify locally
3. Use CONSERVATIVE doses (start low, titrate up)
4. When uncertain, say "Verify with local guidelines"
5. Never claim this is from textbooks - it's LLM-generated guidance
6. Include standard monitoring recommendations
7. List common contraindications
8. If condition is serious, recommend specialist consultation"""

DDX_FALLBACK_SYSTEM = """You are a clinical decision support assistant generating differential diagnosis guidance.

CRITICAL RULES:
1. This is FALLBACK content when textbook evidence is insufficient
2. Always include life-threatening diagnoses in must-not-miss
3. Generate differential based on clinical reasoning, not random guesses
4. Include red flags and when to escalate
5. Recommend appropriate investigations
6. Never claim this is from textbooks - it's LLM-generated guidance
7. When uncertain about a diagnosis, say so explicitly"""

DRUG_FALLBACK_SYSTEM = """You are a clinical pharmacology assistant generating drug information.

CRITICAL RULES:
1. This is FALLBACK content when textbook evidence is insufficient
2. Use CONSERVATIVE dose recommendations
3. Always include major contraindications
4. Always include serious adverse effects
5. Include drug class and mechanism if known
6. Recommend monitoring
7. Never claim this is from textbooks - it's LLM-generated guidance
8. Mark doses with "VERIFY DOSE" warning"""


def generate_fallback_content(
    *,
    feature: str,  # "treatment" | "ddx" | "drug" | "interaction"
    topic: str,
    context: Dict[str, Any],
    sections_needed: List[str],
) -> Dict[str, Any]:
    """
    Generate LLM fallback content when RAG evidence is insufficient.
    
    This content is CLEARLY LABELED as LLM-guided and includes
    verification warnings.
    
    Args:
        feature: Which feature needs fallback
        topic: The medical topic
        context: Patient/clinical context
        sections_needed: Which sections need fallback content
    
    Returns:
        Dict with section content, all labeled as LLM-guided
    """
    if not OPENAI_API_KEY:
        return _empty_fallback_content(sections_needed)
    
    # Select appropriate system prompt
    system_prompts = {
        "treatment": TREATMENT_FALLBACK_SYSTEM,
        "ddx": DDX_FALLBACK_SYSTEM,
        "drug": DRUG_FALLBACK_SYSTEM,
        "interaction": DRUG_FALLBACK_SYSTEM,
    }
    system = system_prompts.get(feature, TREATMENT_FALLBACK_SYSTEM)
    
    # Build user prompt
    user_prompt = _build_fallback_user_prompt(feature, topic, context, sections_needed)
    
    try:
        llm = get_llm()
        resp = llm.chat.completions.create(
            model=LLM_MODEL,
            temperature=0.2,
            messages=[
                {"role": "system", "content": system},
                {"role": "user", "content": user_prompt},
            ],
        )
        raw = resp.choices[0].message.content or ""
        
        # Parse JSON response
        json_match = re.search(r"\{[\s\S]*\}", raw)
        if json_match:
            content = json.loads(json_match.group())
            # Add LLM-guided labels to all content
            return _label_fallback_content(content)
    except Exception as e:
        pass
    
    return _empty_fallback_content(sections_needed)


