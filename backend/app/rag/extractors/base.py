# backend/app/rag/extractors/base.py
"""
Base module for Dual-Mode Response Schema.

DOCTOR GRADE RULE: Every response MUST contain:
1. evidence_based: Content extracted ONLY from RAG sources (never invented)
2. llm_guided: Content generated by LLM when evidence is insufficient
3. coverage: Section scores, fallback status, and reason

The UI MUST render BOTH sections with clear labels:
- "EVIDENCE-BASED" - from textbook sources
- "LLM-GUIDED (VERIFY LOCALLY)" - generated when evidence insufficient

THIS IS NON-NEGOTIABLE.
"""
from __future__ import annotations

import json
import os
import re
from dataclasses import dataclass, field
from enum import Enum
from typing import Any, Dict, List, Optional, TypedDict

from openai import OpenAI


# =============================================================================
# LLM CLIENT
# =============================================================================

LLM_MODEL = os.getenv("OPENAI_CHAT_MODEL", "gpt-4.1")
OPENAI_API_KEY = os.getenv("OPENAI_API_KEY", "")


def get_llm() -> OpenAI:
    """Get OpenAI client."""
    if not OPENAI_API_KEY:
        raise RuntimeError("OPENAI_API_KEY not set")
    return OpenAI(api_key=OPENAI_API_KEY)


# =============================================================================
# SOURCE LABELS
# =============================================================================

class SourceLabel(str, Enum):
    """Source label for content origin."""
    EVIDENCE_BASED = "EVIDENCE-BASED"
    LLM_GUIDED = "LLM-GUIDED (VERIFY LOCALLY)"


# =============================================================================
# COVERAGE TYPES
# =============================================================================

class CoverageInfo(TypedDict):
    """Coverage information for a response."""
    section_scores: Dict[str, float]
    fallback_used: bool
    reason: str
    evidence_chunk_count: int
    fallback_sections: List[str]


# =============================================================================
# DUAL MODE RESPONSE TYPES
# =============================================================================

@dataclass
class LabeledContent:
    """Content with source label."""
    content: Any  # The actual content (string, list, dict, etc.)
    source_label: SourceLabel
    evidence_chunk_ids: List[str] = field(default_factory=list)
    confidence: float = 1.0  # 1.0 for evidence, < 1.0 for LLM-guided


@dataclass
class DualModeSection:
    """A section that can have evidence-based and/or LLM-guided content."""
    key: str
    title: str
    evidence_based: Optional[Any] = None
    llm_guided: Optional[Any] = None
    evidence_chunk_ids: List[str] = field(default_factory=list)
    fallback_used: bool = False
    
    def to_dict(self) -> Dict[str, Any]:
        """Convert to dictionary for JSON serialization."""
        return {
            "key": self.key,
            "title": self.title,
            "evidence_based": self.evidence_based,
            "llm_guided": self.llm_guided,
            "evidence_chunk_ids": self.evidence_chunk_ids,
            "fallback_used": self.fallback_used,
            "source_label": (
                SourceLabel.LLM_GUIDED.value if self.fallback_used and self.llm_guided
                else SourceLabel.EVIDENCE_BASED.value
            ),
        }


# =============================================================================
# DUAL MODE RESPONSE BASE
# =============================================================================

class DualModeResponseBase:
    """
    Base class for dual-mode responses.
    
    Ensures every feature response has:
    - evidence_based: RAG-extracted content
    - llm_guided: Fallback content when evidence insufficient
    - coverage: Section coverage info
    """
    
    def __init__(
        self,
        evidence_based: Dict[str, Any],
        llm_guided: Dict[str, Any],
        coverage: CoverageInfo,
        evidence_chunks: List[Dict[str, Any]] = None,
    ):
        self.evidence_based = evidence_based
        self.llm_guided = llm_guided
        self.coverage = coverage
        self.evidence_chunks = evidence_chunks or []
    
    def to_dict(self) -> Dict[str, Any]:
        """Convert to dictionary for JSON serialization."""
        return {
            "evidence_based": self.evidence_based,
            "llm_guided": self.llm_guided,
            "coverage": self.coverage,
            "evidence": [
                {
                    "chunk_id": ch.get("chunk_id") or ch.get("content_hash") or "",
                    "book": ch.get("book") or ch.get("book_id"),
                    "chapter": ch.get("chapter") or ch.get("section_path"),
                    "page_start": ch.get("page_start"),
                    "snippet": (ch.get("text") or "")[:400],
                }
                for ch in self.evidence_chunks[:25]
            ],
        }
    
    @classmethod
    def merge_sections(
        cls,
        evidence_sections: Dict[str, Any],
        llm_sections: Dict[str, Any],
    ) -> Dict[str, Any]:
        """
        Merge evidence and LLM sections, preferring evidence where available.
        Returns combined dict with source labels.
        """
        merged = {}
        all_keys = set(evidence_sections.keys()) | set(llm_sections.keys())
        
        for key in all_keys:
            ev_val = evidence_sections.get(key)
            llm_val = llm_sections.get(key)
            
            # Determine which value to use and set source label
            if ev_val and _has_content(ev_val):
                merged[key] = {
                    "value": ev_val,
                    "source_label": SourceLabel.EVIDENCE_BASED.value,
                    "fallback_used": False,
                }
            elif llm_val and _has_content(llm_val):
                merged[key] = {
                    "value": llm_val,
                    "source_label": SourceLabel.LLM_GUIDED.value,
                    "fallback_used": True,
                }
            else:
                merged[key] = {
                    "value": None,
                    "source_label": None,
                    "fallback_used": False,
                }
        
        return merged


def _has_content(val: Any) -> bool:
    """Check if a value has meaningful content."""
    if val is None:
        return False
    if isinstance(val, str):
        return bool(val.strip()) and val.lower() not in [
            "not found in sources",
            "insufficient evidence",
            "not specified",
            "none",
            "",
        ]
    if isinstance(val, (list, dict)):
        return bool(val)
    return True


